#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
    bool lemonadeChange(vector<int> &bills)
    {
        int fiveCount = 0, tenCount = 0;
        for (int i = 0; i < bills.size(); i++)
        {
            if (bills[i] == 5)
            {
                fiveCount++;
            }
            else if (bills[i] == 10)
            {
                if (fiveCount)
                {
                    fiveCount--;
                    tenCount++;
                }
                else
                {
                    return false;
                }
            }
            else
            {
                if (tenCount)
                {
                    tenCount--;
                    bills[i] -= 10;
                }
                bills[i] = bills[i] / 5 - 1;
                if (fiveCount >= bills[i])
                {
                    fiveCount -= bills[i];
                }
                else
                {
                    return false;
                }
            }
        }
        return true;
    }
};

